
// 리터럴 복습용


package ch01;

public class LocalVarExam {

	public static void main(String[] args) {
		
		int literal1 = 94 ; 
		int literal2 = 045 ; 
		int literal3 = 0b0110 ; 
		int literal4 = 0xAB3 ; 
		
		System.out.println("10진수 : " + literal1);
		System.out.println("8진수 : " + literal2);
		System.out.println("2진수 : " + literal3);
		System.out.println("16진수 : " + literal4);
		
		
		// literal 리터럴 값+
		
	}

}
